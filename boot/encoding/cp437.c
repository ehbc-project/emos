status_t enc_utf32_to_cp437(wchar_t utf32, uint8_t *cp437out)
{
    uint8_t cp437;

    if (utf32 == 0) {
        cp437 = 0;
    } else if (utf32 < 0x20) {
        return STATUS_INVALID_VALUE;
    } else if (utf32 < 0x7F) {
        cp437 = utf32;
    } else if (utf32 < 0x00A0) {
        return STATUS_INVALID_VALUE;
    } else if (utf32 < 0x0192) {
        if (utf32 >= 0x00A0 + sizeof(unicode_cp437_table_1)) {
            return STATUS_INVALID_VALUE;
        }
        if (!unicode_cp437_table_1[utf32 - 0x00A0]) {
            return STATUS_INVALID_VALUE;
        }
        cp437 = unicode_cp437_table_1[utf32 - 0x00A0];
    } else if (utf32 < 0x2500) {
        switch (utf32 >> 8) {
            case 0x01:
                if (utf32 != 0x0192) return STATUS_INVALID_VALUE;
                cp437 = 0x9F;
                break;
            case 0x03:
                if (utf32 >= 0x0390 + sizeof(unicode_cp437_table_2)) return STATUS_INVALID_VALUE;
                if (!unicode_cp437_table_2[utf32 - 0x0390]) return STATUS_INVALID_VALUE;
                cp437 = unicode_cp437_table_2[utf32 - 0x0390];
                break;
            case 0x20:
                switch (utf32 & 0xFF) {
                    case 0x22: cp437 = 0x07; break;
                    case 0x3C: cp437 = 0x13; break;
                    case 0x7F: cp437 = 0xFC; break;
                    case 0xA7: cp437 = 0x9E; break;
                    default: return STATUS_INVALID_VALUE;
                }
                break;
            case 0x21:
                switch (utf32 & 0xFF) {
                    case 0x90: cp437 = 0x1B; break;
                    case 0x91: cp437 = 0x18; break;
                    case 0x92: cp437 = 0x1A; break;
                    case 0x93: cp437 = 0x19; break;
                    case 0x94: cp437 = 0x1D; break;
                    case 0x95: cp437 = 0x12; break;
                    case 0x98: cp437 = 0x17; break;
                    default: return STATUS_INVALID_VALUE;
                }
                break;
            case 0x22:
                switch (utf32 & 0xFF) {
                    case 0x19: cp437 = 0xF9; break;
                    case 0x1A: cp437 = 0xFB; break;
                    case 0x1E: cp437 = 0xEC; break;
                    case 0x1F: cp437 = 0x1C; break;
                    case 0x29: cp437 = 0xEF; break;
                    case 0x48: cp437 = 0xF7; break;
                    case 0x61: cp437 = 0xF0; break;
                    case 0x64: cp437 = 0xF3; break;
                    case 0x65: cp437 = 0xF2; break;
                    default: return STATUS_INVALID_VALUE;
                }
                break;
            case 0x23:
                switch (utf32 & 0xFF) {
                    case 0x02: cp437 = 0x7F; break;
                    case 0x10: cp437 = 0xA9; break;
                    case 0x20: cp437 = 0xF4; break;
                    case 0x21: cp437 = 0xF5; break;
                    default: return STATUS_INVALID_VALUE;
                }
                break;
            default:
                return STATUS_INVALID_VALUE;
        }
    } else if (utf32 < 0x2630) {
        if (utf32 >= 0x2500 + sizeof(unicode_cp437_table_3)) return STATUS_INVALID_VALUE;
        if (!unicode_cp437_table_3[utf32 - 0x2500]) return STATUS_INVALID_VALUE;
        cp437 = unicode_cp437_table_3[utf32 - 0x2500];
    } else {
        if (utf32 >= 0x2630 + sizeof(unicode_cp437_table_4)) return STATUS_INVALID_VALUE;
        if (!unicode_cp437_table_4[utf32 - 0x2630]) return STATUS_INVALID_VALUE;
        cp437 = unicode_cp437_table_4[utf32 - 0x2630];
    }

    if (cp437out) *cp437out = cp437;
    
    return STATUS_SUCCESS;
}
