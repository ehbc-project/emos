#include <asm/pic.h>

#include <asm/io.h>

void _pc_remap_pic_int(uint8_t master, uint8_t slave)
{
    _i686_out8(0x20, 0x11);
    _i686_out8(0x80, 0x00);
    _i686_out8(0x21, master);
    _i686_out8(0x80, 0x00);
    _i686_out8(0x21, 0x04);
    _i686_out8(0x80, 0x00);
    _i686_out8(0x21, 0x01);
    _i686_out8(0x80, 0x00);
    _i686_out8(0x21, 0x00);
    
    _i686_out8(0xA0, 0x11);
    _i686_out8(0x80, 0x00);
    _i686_out8(0xA1, slave);
    _i686_out8(0x80, 0x00);
    _i686_out8(0xA1, 0x02);
    _i686_out8(0x80, 0x00);
    _i686_out8(0xA1, 0x01);
    _i686_out8(0x80, 0x00);
    _i686_out8(0xA1, 0x00);
}
